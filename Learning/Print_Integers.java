
/**
 *  Printing Integers from 1-10 using for loop.
 *
 * @author (Leoperon)
 * @version (09.05.2019)
 */
public class Print_Integers
{
    public static void main(String[] arguments)
    {
        int c;//Declaring a variables
        for(c=1;c<=10;c++)
        {
            System.out.println(c);
        }
    }
}
